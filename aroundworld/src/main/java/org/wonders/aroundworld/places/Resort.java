package org.wonders.aroundworld.places;

public class Resort 
{
	private String RName;
	private String RCountry;
	private String RRegion;
	private int REstdYear;
	private int RTouristInflow;
	
	public String getRName() 
	{
		return RName;
	}
	public void setRName(String RName) 
	{
		this.RName = RName;
	}
	public String getRCountry() 
	{
		return RCountry;
	}
	public void setRCountry(String RCountry) 
	{
		this.RCountry = RCountry;
	}
	public String getRRegion() 
	{
		return RRegion;
	}
	public void setRRegion(String RRegion) 
	{
		this.RRegion = RRegion;
	}
	public int getREstdYear() 
	{
		return REstdYear;
	}
	public void setREstdYear(int REstdYear) 
	{
		this.REstdYear = REstdYear;
	}
	public int getRTouristInflow() 
	{
		return RTouristInflow;
	}
	public void setRTouristInflow(int RTouristInflow) 
	{
		this.RTouristInflow = RTouristInflow;
	}
}
